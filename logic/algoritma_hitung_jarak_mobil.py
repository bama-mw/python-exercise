# Membaca input dari pengguna
v = float(input("Masukkan kecepatan (v) dalam km/jam: "))  # Kecepatan mobil
t = float(input("Masukkan waktu (t) dalam jam: "))         # Waktu tempuh mobil

# Menghitung jarak yang ditempuh
s = v * t

# Menampilkan jarak yang sudah ditempuh
print(f"Jarak yang ditempuh adalah {s} km.")
